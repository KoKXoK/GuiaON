const btnConfig = document.getElementById('btnConfig');
const configModal = document.getElementById('configModal');
const configOverlay = document.getElementById('configOverlay');
const configClose = document.getElementById('configClose');
const fontSize = document.getElementById('fontSize');
const language = document.getElementById('language');
const theme = document.getElementById('theme');
const configReset = document.getElementById('configReset');
function openConfig() {
  configModal.classList.add('active');
  configOverlay.classList.add('active');
}
function closeConfig() {
  configModal.classList.remove('active');
  configOverlay.classList.remove('active');
}
function applyLanguage(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });
  document.documentElement.lang = lang === 'pt' ? 'pt-BR' : lang === 'en' ? 'en' : 'es';
}
function applyTheme(themeValue) {
  if (themeValue === 'escuro') {
    document.body.classList.add('dark');
  } else if (themeValue === 'claro') {
    document.body.classList.remove('dark');
  } else {
    if (window.matchMedia('(prefers-color-scheme: dark)').matches) {
      document.body.classList.add('dark');
    } else {
      document.body.classList.remove('dark');
    }
  }
}
btnConfig.addEventListener('click', openConfig);
configClose.addEventListener('click', closeConfig);
configOverlay.addEventListener('click', closeConfig);
language.addEventListener('change', function() {
  applyLanguage(this.value);
  localStorage.setItem('language', this.value);
});
theme.addEventListener('change', function() {
  applyTheme(this.value);
  localStorage.setItem('theme', this.value);
});
fontSize.addEventListener('change', function() {
  if (this.value === 'pequena') {
    document.body.style.fontSize = '14px';
  } else if (this.value === 'grande') {
    document.body.style.fontSize = '18px';
  } else {
    document.body.style.fontSize = '16px';
  }
  localStorage.setItem('fontSize', this.value);
});
configReset.addEventListener('click', function() {
  language.value = 'pt';
  theme.value = 'auto';
  fontSize.value = 'normal';
  applyLanguage('pt');
  applyTheme('auto');
  document.body.style.fontSize = '16px';
  localStorage.removeItem('language');
  localStorage.removeItem('theme');
  localStorage.removeItem('fontSize');
});
window.addEventListener('load', function() {
  const savedLang = localStorage.getItem('language') || 'pt';
  const savedTheme = localStorage.getItem('theme') || 'auto';
  const savedFontSize = localStorage.getItem('fontSize') || 'normal';
  language.value = savedLang;
  theme.value = savedTheme;
  fontSize.value = savedFontSize;
  applyLanguage(savedLang);
  applyTheme(savedTheme);
  if (savedFontSize === 'pequena') document.body.style.fontSize = '14px';
  else if (savedFontSize === 'grande') document.body.style.fontSize = '18px';
  else document.body.style.fontSize = '16px';
});
window.matchMedia('(prefers-color-scheme: dark)').addEventListener('change', function() {
  const savedTheme = localStorage.getItem('theme');
  if (!savedTheme || savedTheme === 'auto') {
    applyTheme('auto');
  }
});
